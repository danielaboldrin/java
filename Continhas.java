public class Continhas {
    public static void main(String[] args) {
        
        int a;
        int b; 
        int c;
        
        a = 19;
        b = 3;
        c = a + b;
        System.out.println("Valor da Soma = " + c);
        c = a - b;
        System.out.println("Valor da Subtracao = " + c);
        c = a * b;
        System.out.println("Valor de Multiplicacao = " + c);
        c = a / b;
        System.out.println("Valor de Divisao = " + c);
        c = a % b;
        System.out.println("Valor de Resto = " + c);
    }
}